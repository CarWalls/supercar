// Helper to ensure direct image URLs for Pexels links
function processUrl(url) {
    if (!url) return "";
    // If it's a Pexels page link, convert it to a direct image link
    if (url.includes("pexels.com/photo/")) {
        const id = url.split("/").filter(Boolean).pop().split("-").pop();
        if (!isNaN(id)) {
            return `https://images.pexels.com/photos/${id}/pexels-photo-${id}.jpeg`;
        }
    }
    return url;
}

// Debounce helper function
function debounce(func, wait) {
    let timeout;
    return function executedFunction(...args) {
        const later = () => {
            clearTimeout(timeout);
            func(...args);
        };
        clearTimeout(timeout);
        timeout = setTimeout(later, wait);
    };
}

// Initialize Gallery
function initGallery() {
    if (!wallpaperGrid) return;
    // Show loading state
    const isFirstLoad = wallpaperGrid.innerHTML === "";

    const filteredWallpapers = shuffledWallpapers.filter((w) => {
        // Automatically fix URLs on the fly for better manual entry support
        if (w.url && w.url.includes("pexels.com/photo/")) {
            w.url = processUrl(w.url);
        }

        const matchesFilter =
            currentFilter === "all" || w.category === currentFilter;
        const matchesSearch = (w.title || "")
            .toLowerCase()
            .includes(searchQuery.toLowerCase());
        return matchesFilter && matchesSearch;
    });

    // Clear grid
    wallpaperGrid.innerHTML = "";

    // If no results, show message
    if (filteredWallpapers.length === 0) {
        wallpaperGrid.innerHTML = '<p style="grid-column: 1/-1; text-align: center; padding: 3rem; color: var(--text-dim);">No wallpapers found. Try a different search or filter.</p>';
        return;
    }

    // Batch render images in chunks to avoid blocking the UI
    const BATCH_SIZE = 12; // Render 12 images at a time
    let currentBatch = 0;

    function renderBatch() {
        const start = currentBatch * BATCH_SIZE;
        const end = Math.min(start + BATCH_SIZE, filteredWallpapers.length);

        const fragment = document.createDocumentFragment();

        for (let i = start; i < end; i++) {
            const wallpaper = filteredWallpapers[i];
            const card = document.createElement("div");
            card.className = `wallpaper-card ${wallpaper.category} reveal`;
            card.style.transitionDelay = `${(i % 4) * 0.1}s`;

            // Make the whole card clickable for preview
            card.onclick = () => openPreview(wallpaper.url, wallpaper.title);

            // Optimized Thumbnail URL (only apply Pexels params if it's a Pexels URL)
            let thumbnailUrl = wallpaper.url;
            if (thumbnailUrl && thumbnailUrl.includes("pexels.com") && !thumbnailUrl.includes("?")) {
                thumbnailUrl += "?auto=compress&cs=tinysrgb&w=400&q=60";
            }

            card.innerHTML = `
                <img src="${thumbnailUrl || ''}" alt="${wallpaper.title} Ultra HD 4K Car Wallpaper" loading="lazy">
                <div class="card-overlay">
                    <div class="card-info">
                        <h3>${wallpaper.title || 'Untitled'}</h3>
                    </div>
                </div>
            `;
            fragment.appendChild(card);
            observer.observe(card);
        }

        wallpaperGrid.appendChild(fragment);

        currentBatch++;

        // Continue rendering if there are more batches
        if (end < filteredWallpapers.length) {
            requestAnimationFrame(renderBatch);
        }
    }

    // Start rendering
    renderBatch();
}

// Scroll Reveal Logic
const observerOptions = {
    threshold: 0, // Trigger as soon as one pixel is visible
    rootMargin: "0px 0px 200px 0px", // Trigger 200px before it enters the viewport
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
        if (entry.isIntersecting) {
            entry.target.classList.add("active");
            observer.unobserve(entry.target); // Stop observing once revealed
        }
    });
}, observerOptions);

