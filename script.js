document.addEventListener("DOMContentLoaded", () => {
    initGallery();
    initScrollReveal();

    // Cross-browser Smooth Scroll for Anchors
    document.querySelectorAll("a").forEach((anchor) => {
        const href = anchor.getAttribute("href");
        if (href && href.includes("#")) {
            anchor.addEventListener("click", function (e) {
                const targetId = href.split("#")[1];
                const targetElement = document.getElementById(targetId);

                // If the link is just a hash or points to an element on this page
                if (
                    targetElement &&
                    (href.startsWith("#") ||
                        href.split("#")[0] === window.location.pathname.split("/").pop() ||
                        (href.split("#")[0] === "index.html" &&
                            window.location.pathname === "/"))
                ) {
                    e.preventDefault();
                    const headerOffset = 80;
                    const elementPosition = targetElement.getBoundingClientRect().top;
                    const offsetPosition =
                        elementPosition + window.pageYOffset - headerOffset;

                    window.scrollTo({
                        top: offsetPosition,
                        behavior: "smooth",
                    });

                    // Close mobile menu if open
                    if (nav && nav.classList.contains("active")) {
                        menuToggle.classList.remove("active");
                        nav.classList.remove("active");
                        document.body.style.overflow = "";
                    }
                }
            });
        }
    });

    // Mobile Menu Toggle
    const menuToggle = document.getElementById("menuToggle");
    const nav = document.querySelector("nav");
    const navLinks = document.querySelectorAll("nav a");

    if (menuToggle && nav) {
        menuToggle.addEventListener("click", () => {
            menuToggle.classList.toggle("active");
            nav.classList.toggle("active");
            document.body.style.overflow = nav.classList.contains("active")
                ? "hidden"
                : "";
        });

        // Close menu when link is clicked
        navLinks.forEach((link) => {
            link.addEventListener("click", () => {
                menuToggle.classList.remove("active");
                nav.classList.remove("active");
                document.body.style.overflow = "";
            });
        });
    }

    // Scroll Progress Update
    const scrollProgressBar = document.getElementById("scrollProgress");
    window.addEventListener("scroll", () => {
        const winScroll =
            document.body.scrollTop || document.documentElement.scrollTop;
        const height =
            document.documentElement.scrollHeight -
            document.documentElement.clientHeight;
        const scrolled = (winScroll / height) * 100;
        if (scrollProgressBar) {
            scrollProgressBar.style.width = scrolled + "%";
        }

        // Header glass effect on scroll
        if (window.scrollY > 50) {
            header.classList.add("scrolled");
        } else {
            header.classList.remove("scrolled");
        }

        // Sticky controls shrink effect
        const stickyControls = document.querySelector(".sticky-controls");
        if (stickyControls) {
            const galleryTop = document.getElementById("gallery").offsetTop;
            if (window.scrollY > galleryTop - 100) {
                stickyControls.classList.add("scrolled");
            } else {
                stickyControls.classList.remove("scrolled");
            }
        }
    });
});
